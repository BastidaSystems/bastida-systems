const root = document.documentElement;
const storedLang = localStorage.getItem("portfolioLanguage") || "en";
const storedTheme = localStorage.getItem("portfolioTheme") || "light";

function applyLanguage(language) {
  const next = translations[language] ? language : "en";
  root.lang = next;
  localStorage.setItem("portfolioLanguage", next);

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    if (translations[next][key]) {
      node.textContent = translations[next][key];
    }
  });

  document.querySelectorAll("[data-lang]").forEach((button) => {
    button.classList.toggle("is-active", button.dataset.lang === next);
  });
}

function applyTheme(theme) {
  const next = theme === "dark" ? "dark" : "light";
  root.dataset.theme = next;
  localStorage.setItem("portfolioTheme", next);

  const label = document.querySelector(".theme-toggle [data-i18n]");
  if (label) {
    const dictionary = translations[root.lang] || translations.en;
    label.textContent = next === "dark" ? dictionary["theme.light"] : dictionary["theme.label"];
  }
}

document.querySelectorAll("[data-lang]").forEach((button) => {
  button.addEventListener("click", () => {
    applyLanguage(button.dataset.lang);
    applyTheme(root.dataset.theme);
  });
});

document.querySelector(".theme-toggle")?.addEventListener("click", () => {
  applyTheme(root.dataset.theme === "dark" ? "light" : "dark");
});

const observer = new IntersectionObserver((entries) => {
  entries.forEach((entry) => {
    if (entry.isIntersecting) {
      entry.target.classList.add("is-visible");
      observer.unobserve(entry.target);
    }
  });
}, { threshold: 0.14 });

document.querySelectorAll(".reveal").forEach((node) => observer.observe(node));

applyLanguage(storedLang);
applyTheme(storedTheme);
